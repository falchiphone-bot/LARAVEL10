        <h2 class="text-4xl font-bold text-center mb-6"> ANO DE 2018</h2>


Em abril de 2018, iniciei a captação de atletas e escola de futebol iniciado em abril de 2018, com um ex- jogador profissional da cidade de Votuporanga.
Visitando clubes grandes do cenário brasileiro, vi a necessidade de possuir um clube para poder ficar amparado nas formações de atletas de futebol.


<hr>
<h2 class="text-4xl font-bold text-center mb-6"> ANO DE 2019</h2>
Em janeiro de 2019, assumi o TANABI ESPORTE CLUBE, juntamente com uma comissão de amigos a qual prestaram serviços remunerados ao clube, como seguimento dos trabalhos anteriores a qual veio completar.
O clube representou nos jogos regionais e jogos abertos de 2019 do Estado de São Paulo a cidade de Tanabi, a qual veio se consagrar campeão dos Jogos Regionais na cidade de Andradina e posteriormente campeão do Jogos Abertos do Estado de São Paulo de 2019 na cidade de Marília.

<hr>
<h2 class="text-4xl font-bold text-center mb-6"> ANO DE 2020</h2>

Em 2020, sediamos na cidade de Tanabi a COPA SÃO PAULO DE FUTEBOL JÚNIOR – COPINHA e participamos até a segunda fase da mesma e disputamos o Campeonato Paulista da Série B de futebol profissional, com participação discreta.


<hr>
<h2 class="text-4xl font-bold text-center mb-6"> ANO DE 2021</h2>

Em 2021, disputamos o Campeonato Paulista da Primeira Divisão sub-20, com participação regular e fomos até a segunda fase.


<hr>
<h2 class="text-4xl font-bold text-center mb-6"> ANO DE 2022</h2>

Em 2022, sediamos na cidade de Tanabi a COPA SÃO PAULO DE FUTEBOL JÚNIOR – COPINHA e não passamos da primeira fase.
O clube representou nos jogos regionais e jogos abertos de 2022 do Estado de São Paulo a cidade de Tanabi, a qual veio se consagrar campeão dos Jogos Regionais com a final na cidade de São José do Rio Preto e posteriormente campeão do Jogos Abertos do Estado de São Paulo de 2022 na cidade de São Sebastião.


Disputamos Campeonato Paulista sub-15 e sub-17 e sub-20. O sub-15, não passou da primeira fase, porém o sub-17 participou até a  segunda fase enfrentando grandes clubes como Ferroviária e Santos. O sub-20, classificou-se para segunda fase, como segundo colocado no grupo, empatado em pontos com o Mirassol,  primeiro colocado. Os confrontos da segunda fase foram: Ferroviária, Santos e São José.  Fomos bem e disputamos a classificação no último jogo a qual empatamos em Araraquara com a Ferroviária em 3 a 3. Assim não conseguindo a classificação para terceira fase.
Em agosto de 2022, iniciou o sub-11 e sub-13, a qual o TANABI ESPORTE CLUBE, participou através de parceira com escolinha de futebol de São José do Rio Preto, a WS Sports.



<hr>
<h2 class="text-4xl font-bold text-center mb-6"> ANO DE 2023</h2>

Em 2023, sediamos na cidade de Tanabi a COPA SÃO PAULO DE FUTEBOL JÚNIOR – COPINHA e passamos da primeira fase com 100% de aproveitamento. Três jogos e três vitórias. Eliminando grandes clubes como Portuguesa de São Paulo e Náutico de Pernambuco. Perdemos por 1 a 0 para o Chapecoense e fomos eliminado na segunda fase.

<hr>
<h2 class="text-4xl font-bold text-center mb-6"> ANO DE 2024</h2>

Em 2024, sediamos na cidade de Tanabi a COPA SÃO PAULO DE FUTEBOL JÚNIOR – COPINHA e passamos da primeira fase com 100% de aproveitamento. Três jogos e duas  vitórias e empate com Ponte Preta. Eliminando grandes clubes como Portuguesa de São Paulo e Náutico de Pernambuco. Perdemos por 1 a 0 para o Chapecoense e fomos eliminado na segunda fase.

<hr>
<h2 class="text-4xl font-bold text-center mb-6"> ANO DE 2025</h2>
Em 2025, sediamos na cidade de Tanabi a COPA SÃO PAULO DE FUTEBOL JÚNIOR – COPINHA e passamos da primeira fase com 40% de aproveitamento. Três jogos e uma  vitória, um empate e uma derrota. Eliminado na primeira fase.


<h2 class="text-4xl font-bold text-center mb-6"> ENTRE O ANO DE 2018 A 2025</h2>
O clube representou nos jogos regionais e jogos abertos de 2023 do Estado de São Paulo a cidade de Tanabi, a qual veio se consagrar campeão dos Jogos Regionais com a final na cidade de São José do Rio Preto e aguardando para disputar os Jogos Abertos do Estado de São Paulo de 2023 que será na cidade de São José do Rio Preto, de 04 a 09 de outubro de 2023. Com objetivo de alcançarmos o tricampeonato.
Também em 2023, o Tanabi Esporte Clube representou a cidade Tanabi nos Jogos da Juventude sub-17, a qual veio a se consagrar campeão com final na cidade de Mira Estrela-SP.
Considerando o acima, o Tanabi Esporte Clube, por meio de seu Presidente e Controlador do TANABI ESPORTE CLUBE – SAF, Pedro Roberto Falchi, conforme ATA DA ASSEMBLÉIA EXTRAORDINÁRIA DO TANABI ESPORTE CLUBE - GESTÃO 2021/2024 - PARA DISSOLUÇÃO DA ASSOCIAÇÃO CIVIL E TRANSFORMAÇÃO INTEGRAL EM SOCIEDADE ANÔNIMA DO FUTEBOL (S.A.F), CONFORME LEI 14.193/2021, DE 06 DE AGOSTO DE 2021, em fase de registro junto ao Oficial de Registro de Pessoas Jurídica – Comarca de TANABI – SP.
Dessa forma, viabilizando que ocorram investimentos e demais aportes permitidos pela Lei 14.193/2021 de 06 de agosto 2021, enquanto sociedade de direito privado, portanto, aberto a propostas de AQUISIÇÃO DE QUOTAS DE AÇÕES ORDINÁRIAS e PREFERENCIAIS do Tanabi Esporte Clube, bem como seus direitos Federativos, Econômicos e Registro junto a Federação Paulista de Futebol.
Atualmente, o clube, conta com cessão de uso do estádio Municipal Alberto Victolo, aprovada em Câmara de Vereadores, em 2019, sem data de término prevista. Em anexo no final desta.
Atualmente, o TANABI ESPORTE CLUBE – SAF,
conta uma carteira de jogadores, composta de atletas amadores e atletas profissionais .
Em preparação para solicitação junto a Federação Paulista de futebol e Confederação Brasileira de Futebol para contemplação do selo de Clube Formador.
