                <h2 class="text-4xl font-bold mb-4">Histórico</h2>
                 <p>O Tanabi SAF é um clube brasileiro de futebol de Tanabi, interior de São Paulo. Fundado em 18 de dezembro de 1942, instituiu-se Sociedade Anônima do Futebol em 18 de dezembro de 2024, com CNPJ 45.157.559/0001-68 com o código CBF da organização esportiva 00031SP.</p>


                <hr>

                <h2 class="text-4xl font-bold mb-4">Resumo</h2>
                <h4>A LEI Nº 14.193, DE 06 DE AGOSTO DE 2021</h4>
                <p>Instituí a Sociedade Anônima do Futebol e dispõe sobre normas de constituição, governança, controle e transparência, meios de financiamento da atividade futebolística, tratamento dos passivos das entidades de práticas desportivas e regime tributário específico.</p>
                <p>Art. 28. A Sociedade Anônima do Futebol deverá instituir Programa de Desenvolvimento Educacional e Social (PDE), para, em convênio com instituição pública de ensino, promover medidas em prol do desenvolvimento da educação, por meio do futebol, e do futebol, por meio da educação.  </p>

                    <p> § 1º A Sociedade Anônima do Futebol poderá investir, no âmbito das obrigações do Plano de Desenvolvimento Educacional e Social, mas não exclusivamente:  </p>

                        <p> I - Na reforma ou construção de escola pública, bem como na manutenção de quadra ou campo destinado à prática do futebol;  </p>

                            <p>II - Na instituição de sistema de transporte dos alunos qualificados à participação no convênio, na hipótese de a quadra ou o campo não se localizar nas dependências da escola;  </p>

                                <p>III - Na alimentação dos alunos durante os períodos de recreação futebolística e de treinamento;  </p>

                                    <p>IV - Na capacitação de ex-jogadores profissionais de futebol, para ministrar e conduzir as atividades no âmbito do convênio;  </p>

                                        <p> V - Na contratação de profissionais auxiliares, especialmente de preparadores físicos, nutricionistas e psicólogos, para acompanhamento das atividades no âmbito do convênio;  </p>

                                            <p> VI - Na aquisição de equipamentos, materiais e acessórios necessários à prática esportiva.  </p>

                                                <p>§ 2º Somente se habilitarão a participar do convênio alunos regulamente matriculados na instituição conveniada e que mantenham o nível de assiduidade às aulas regulares e o padrão de aproveitamento definidos no convênio.  </p>

                                                    <p> § 3º O Programa de Desenvolvimento Educacional e Social deverá oferecer, igualmente, oportunidade de participação às alunas matriculadas em escolas públicas, a fim de realizar o direito de meninas terem acesso ao esporte.  </p>

                                                        <p>Art.29 Além das obrigações constantes na Lei nº 9.615, de 24 de março de 1998, para as entidades de práticas
                                                            desportivas formadoras de atletas e das disposições desta Seção, a Sociedade Anônima do Futebol proporcionará ao atleta em formação que morar em alojamento por ela mantido:  </p>
                                                            <p> I.	Instalações físicas certificadas pelos órgãos e autoridades competentes com relação à habilitação,
                                                                à higiene, à salubridade e às medidas de prevenção e combate a incêndio e a desastres;  </p>
                                                                <p> II.	Assistência de monitor responsável durante todo o dia;  </p>
                                                                    <p> III.	Convivência familiar;  </p>
                                                                        <p> IV.	Participação em atividade culturais e de lazer nos horários livres; e  </p>
                                                                            <p>  V.	Assistência religiosa àqueles que desejam, de acordo com suas crenças.</p>

                <hr>

               <h2 class="text-4xl font-bold mb-4">Introdução</h2>
                <p>Partindo desta premissa, o Tanabi Sociedade Anônima do Futebol estabelece um Plano de Desenvolvimento Educacional e Social, para nortear suas ações junto à sociedade, utilizando o esporte como ferramenta complementar no desenvolvimento educacional e social de crianças e adolescentes.</p>


                <h2 class="text-4xl font-bold mb-4">Visão</h2>
                <p>Transformar o Tanabi Sociedade Anônima do Futebol em referência no segmento, encorajando mediante resultados esportivos e sociais a outros a seguirem nossos passos na transformação da sociedade através do esporte.</p>

                <hr>


                <h2 class="text-4xl font-bold mb-4">Missão</h2>
                <p>Acreditamos que o esporte, especialmente o futebol, pode proporcionar um ser humano saudável e disciplinado para a sociedade. Temos o compromisso de estabelecer programas de treinamento esportivo que atinjam essas expectativas.</p>

                <p>Entendemos que vidas podem ser transformadas pelo esporte, o convívio em sociedade e o distanciamento das drogas.</p>

                <p>Fornecer educação esportiva de qualidade para crianças e adolescentes, propiciar o desenvolvimento saudável dos alunos, combater o sedentarismo e obesidade infantil, incentivar a prática esportiva e dar oportunidade aos jogadores com talento. </p>


                <hr>


                <h2 class="text-4xl font-bold mb-4">Pontos fortes</h2>
